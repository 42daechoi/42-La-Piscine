int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb == 0)
		return (0);
	while (1)
	{
		i++;
		if (i * i == nb)
			return (i);
		if (i > 46340)
			break ;
	}
	return (0);
}
